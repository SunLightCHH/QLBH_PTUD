        // Cập nhật thông tin sản phẩm
        if (vtHienTai >= 0) {
            dsSanPham.get(vtHienTai).setId(Idtxt.getText());
            dsSanPham.get(vtHienTai).setNameproduct(NameProductstxt.getText());
            dsSanPham.get(vtHienTai).setMau((Mautxt.getText()));
            dsSanPham.get(vtHienTai).setNgaynhap(ngayNhaptxt.getText());
            dsSanPham.get(vtHienTai).setGiaban(Float.parseFloat(giaBantxt.getText()));
            dsSanPham.get(vtHienTai).setGiamua(Float.parseFloat(giaMuatxt.getText()));

            ChucNangSP.updateProducts(dsSanPham.get(vtHienTai));

        }
