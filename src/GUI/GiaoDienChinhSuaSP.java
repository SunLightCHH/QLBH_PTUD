    private void showData() {
        tableModel.setRowCount(0);
        for (SanPham product : dsSanPham) {
            tableModel.addRow(new Object[]{
                tableModel.getRowCount() + 1,
                product.getId(),
                product.getNameproduct(),
                product.getMau(),
                product.getSoluong(),
                product.getGiaban(),
                product.getGiamua()
            });
        }
    }

